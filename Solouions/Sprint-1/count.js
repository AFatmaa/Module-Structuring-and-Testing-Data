let count = 0;

count = count + 1;

// Line 1 is a variable declaration, creating the count variable with an initial value of 0
// Line 3 is performing an assignment operation. The = operator is assigning a new value to the count variable.
// The new value is the result of the expression on the right side: count + 1.
// This operation is incrementing the count variable by 1, so after this line, count will be 1.

console.log(`The value of count is now: ${count}`);
